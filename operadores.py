# operadores matematicos
"""
- soma (+) 
- subtração (-) 
- multiplicação (*)
- divisao (/)
- divisao inteira (//)
- resto da divisao (%)
- potencia (**)
""" 
numero1 = 10
numero2 = 20
print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2)
print(20 % 3)
print(2 ** 3)

